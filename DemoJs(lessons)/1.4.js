// Напиши код який виводить в консоль округлення вверх / вниз.Використовуй методи`Math.floor()`,
// `Math.ceil()` і`Math.round()`.
// Math.ceil;
// Math.floor;
// Math.round;
// Math.pow
// Math.sqrt


const value = 27.002;
console.log(Math.ceil(value)); // 28 - округляет в большую сторону(даже если будет 27,01 = 28)
console.log(Math.floor(value)); // 27 - округляет в меньшую сторону(даже если будет 27,01 = 27)
console.log(Math.round(value)); // 28 - если 27,5 = 28, если 27,01 = 27
console.log(Math.pow(value, 2)); //  - ставит число в степень
